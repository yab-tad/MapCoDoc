"""
Trace-link recovery accuracy from the manual adjudication labels.

Reads the per-library adjudication CSVs (tests/<lib>_<src>_fidelity_review.csv) and
computes the trace-link recovery accuracy table (paper Table: trace-link recovery
accuracy by library and modality) plus the error attribution split
(localization / structuring / residual).

Outputs:
  tests/trace_link_accuracy.csv      per library x source: correct / total / accuracy
  tests/trace_link_accuracy.tex      IEEEtran table
  tests/trace_link_adjudication.csv  consolidated per-instance verdicts: one row per
                                     library x source x member (2,884 rows)

Usage:
  python tests/trace_link_accuracy.py
"""
import csv
import os
from collections import Counter

HERE = os.path.dirname(os.path.abspath(__file__))
LIBS = ["numpy", "pandas", "requests", "sklearn", "sqlalchemy", "torch", "xgboost"]
LIB_LABEL = {
    "numpy": "NumPy", "pandas": "Pandas", "requests": "Requests",
    "sklearn": "scikit-learn", "sqlalchemy": "SQLAlchemy", "torch": "PyTorch",
    "xgboost": "XGBoost",
}
SOURCES = ["web", "pdf"]

# Paper vocabulary for error attribution (idempotent on already-mapped values).
ROOT_CAUSE_MAP = {
    "retrieval": "localization",
    "structuring/empty": "structuring",
    "review": "residual",
}


def load(lib, src):
    path = os.path.join(HERE, f"{lib}_{src}_fidelity_review.csv")
    with open(path, encoding="utf-8-sig") as fh:
        return list(csv.DictReader(fh))


def main():
    rows_out = []
    grand = {s: [0, 0] for s in SOURCES}   # correct, total
    causes = Counter()
    per_member = []                        # one row per library x source x member

    for lib in LIBS:
        rec = {"library": lib}
        for src in SOURCES:
            rows = load(lib, src)
            total = len(rows)
            correct = sum(1 for r in rows
                          if (r.get("correct_doc") or "").strip().lower() == "yes")
            rec[f"{src}_correct"] = correct
            rec[f"{src}_total"] = total
            rec[f"{src}_accuracy_pct"] = round(100.0 * correct / total, 1)
            grand[src][0] += correct
            grand[src][1] += total
            for r in rows:
                verdict = (r.get("correct_doc") or "").strip().lower()
                cause = (r.get("root_cause_layer") or "").strip()
                cause = ROOT_CAUSE_MAP.get(cause, cause)
                if verdict == "no":
                    causes[cause] += 1
                # note column: curated package CSVs carry "adjudication_note";
                # the raw dev-tree review CSVs carry "human_note"
                note = (r.get("adjudication_note") or r.get("human_note") or "").strip()
                per_member.append({
                    "library": lib,
                    "source": src,
                    "api_name": (r.get("api_name") or "").strip(),
                    "member_type": (r.get("member_type") or "").strip(),
                    "correct_doc": verdict,
                    "scope": (r.get("scope") or "").strip(),
                    "root_cause_layer": cause,
                    "adjudication_note": note,
                })
        rows_out.append(rec)

    # ---- CSV ----
    out_csv = os.path.join(HERE, "trace_link_accuracy.csv")
    with open(out_csv, "w", newline="", encoding="utf-8") as fh:
        w = csv.DictWriter(fh, fieldnames=list(rows_out[0].keys()))
        w.writeheader()
        w.writerows(rows_out)
        w.writerow({"library": "ALL",
                    "web_correct": grand["web"][0], "web_total": grand["web"][1],
                    "web_accuracy_pct": round(100.0 * grand["web"][0] / grand["web"][1], 1),
                    "pdf_correct": grand["pdf"][0], "pdf_total": grand["pdf"][1],
                    "pdf_accuracy_pct": round(100.0 * grand["pdf"][0] / grand["pdf"][1], 1)})

    # ---- consolidated per-instance verdicts ----
    out_adj = os.path.join(HERE, "trace_link_adjudication.csv")
    with open(out_adj, "w", newline="", encoding="utf-8") as fh:
        w = csv.DictWriter(fh, fieldnames=list(per_member[0].keys()))
        w.writeheader()
        w.writerows(per_member)

    # ---- console ----
    print("Trace-link recovery accuracy (manual adjudication)")
    print(f"{'library':13s} {'web':>18s} {'pdf':>18s}")
    for rec in rows_out:
        wtxt = f"{rec['web_correct']}/{rec['web_total']} ({rec['web_accuracy_pct']}%)"
        ptxt = f"{rec['pdf_correct']}/{rec['pdf_total']} ({rec['pdf_accuracy_pct']}%)"
        print(f"{rec['library']:13s} {wtxt:>18s} {ptxt:>18s}")
    for src in SOURCES:
        c, t = grand[src]
        print(f"overall {src}: {c}/{t} = {100.0 * c / t:.1f}%")
    print("error attribution:", dict(causes))

    # ---- LaTeX ----
    tex = [
        "% Auto-generated by tests/trace_link_accuracy.py",
        r"\begin{table}[t]",
        r"\centering",
        r"\caption{Trace-link recovery accuracy by library and modality.}",
        r"\label{tab:trace-accuracy}",
        r"\small",
        r"\begin{tabular}{lrr}",
        r"\toprule",
        r"Library & Web & PDF \\",
        r"\midrule",
    ]
    for rec in rows_out:
        name = LIB_LABEL[rec["library"]]
        tex.append(
            f"\\texttt{{{name}}} & {rec['web_correct']}/{rec['web_total']} "
            f"({rec['web_accuracy_pct']}\\%) & {rec['pdf_correct']}/{rec['pdf_total']} "
            f"({rec['pdf_accuracy_pct']}\\%) \\\\")
    wc, wt = grand["web"]
    pc, pt = grand["pdf"]
    tex += [
        r"\midrule",
        f"\\textbf{{Overall}} & \\textbf{{{wc}/{wt} ({100.0 * wc / wt:.1f}\\%)}} & "
        f"\\textbf{{{pc}/{pt} ({100.0 * pc / pt:.1f}\\%)}} \\\\",
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{table}",
    ]
    out_tex = os.path.join(HERE, "trace_link_accuracy.tex")
    with open(out_tex, "w", encoding="utf-8") as fh:
        fh.write("\n".join(tex) + "\n")
    print(f"\nwrote {out_csv}\nwrote {out_tex}"
          f"\nwrote {out_adj} ({len(per_member)} member-source instances)")


if __name__ == "__main__":
    main()
